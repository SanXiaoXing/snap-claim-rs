import "@testing-library/jest-dom/vitest";

// jsdom 不实现 matchMedia，gsap.matchMedia() 依赖它
if (!window.matchMedia) {
  window.matchMedia = (query: string) => ({
    matches: false,
    media: query,
    onchange: null,
    addListener: () => {},
    removeListener: () => {},
    addEventListener: () => {},
    removeEventListener: () => {},
    dispatchEvent: () => false,
  });
}

